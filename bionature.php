<p align="left">El biogás es un gas combustible compuesto principalmente por metano y dióxido de carbono, y se puede producir (entre otros procesos) durante la degradación de Residuos Sólidos Urbanos (RSU) en rellenos sanitarios gracias al metabolismo de bacterias que consumen en ausencia de aire la materia orgánica (biomasa) presente en los desechos. La importancia del biogás radica en que es una fuente de Energía Renovable No Convencional (ERNC) y como tal, se puede utilizar para producir energía eléctrica sin la necesidad de recurrir a combustibles fósiles como el gas natural, petróleo o carbón, los cuales provocan la emisión de gases de efecto invernadero.  
<br/>
<br />
     Esta aplicación te permitirá estimar cuánto biogás y energía pueden producirse a partir de los desechos que eliminas mensualmente de tu hogar y saber a cuánto consumo de electricidad equivale. De esta forma, podrás tener una mejor visión del gran potencial de desarrollar en nuestro país ERNC a partir del biogás y con ello contribuir al aprovechamiento y valorización de los desechos industriales y domésticos.
